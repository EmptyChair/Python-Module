### Built-in math functions in Python

# min/max value in a list
x = min(5,10,125,25)
y = max(5,10,125,25)

print(x)
print(y)

# absolute value

print(abs(-7))

# potence/power x^y

print(pow(2,3))

### MATH module

import math

# square root
x = math.sqrt(64)
print(x)

# math.ceil() method rounds a number upwards to its nearest integer
# math.floor() method rounds a number downwards to its nearest integer

print(math.ceil(1.4))
print(math.floor(1.4))

# pi

print(math.pi)
