x = 5
print("type of x: ",type(x))

x = str(5)
print("X :",x)
print("type of x: ",type(x))
x = int(5)
print("X :",x)
print("type of x: ",type(x))
x = float(5)
print("X :",x)
print("type of x: ",type(x))
x = range(5)
print("X :",x)
print("type of x: ",type(x))
x = list((5, 6))
print("X :",x)
print("type of x: ",type(x))
x = bool(4) # not a zero, thus true
print("X :",x)
print("type of x: ",type(x))

